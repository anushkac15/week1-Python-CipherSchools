# slicing / selecting sub sequences
# syntax - [start argument : stop argument -1 : step]
print("anushka"[2:5])
print("anushka"[0:5:1])
print("anushka"[0:5:2])
print("anushka"[0::2])
print("anushka"[0::3])
print("anushka"[::4])
print("anushka"[::-1])
print("anushka"[5::-1])
print("anushka"[-1::-1])
print("anushka"[::-1]) # trick